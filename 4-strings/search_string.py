titulo_curso = "curso profesional de Python para trabajar con python3 en Meli"

# metodo para encontrar un sting
contador = titulo_curso.count("python")
print(contador)

# usando la palabra reservada "in"
print("python" in titulo_curso.lower())

# metodo "startswith" y "endswith"
print(titulo_curso.startswith("cur"))
print(titulo_curso.endswith("Meli"))
